public class datatypes {
    public static void main(String[] args) {
        int a =12;
        boolean flag = true;
        short s = 1200;
        byte b = 12;
        double d = 7676;
        char ch = 'p';
        float fl =  (float) 123.54;

        System.out.println("The integer varible is " + a);
        System.out.println("The boolean varible is " + flag);
        System.out.println("The short varible is " + s);
        System.out.println("The byte varible is " + b);
        System.out.println("The double varible is " + d);
        System.out.println("The character varible is " + ch);
        System.out.println("The float varible is " + fl);
    }
    
}
